package com.example.basic;

public class Demo04Float {
    public static void main(String[] args) {
        basic();
    }

    public static void basic() {
        double x = 1.0 / 10;
        double y = 1 - 9.0 / 10;
        // 观察x和y是否相等:
        System.out.println(x);
        System.out.println(y);
    }
}
